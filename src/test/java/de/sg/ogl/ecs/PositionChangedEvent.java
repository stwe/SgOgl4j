package de.sg.ogl.ecs;

public class PositionChangedEvent extends Event {

    int x = 0;
    int y = 0;

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
}
